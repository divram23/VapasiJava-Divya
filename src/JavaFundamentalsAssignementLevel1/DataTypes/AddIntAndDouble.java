package JavaFundamentalsAssignementLevel1;

public class AddIntAndDouble {
    public static void main(String[] args) {
        int num1 = 5;
        double num2 = 6.2;
        int sum1 = (int) (num1 + num2);
        double sum2 = num1 + num2;
        System.out.println("Sum of Integer: "+sum1);
        System.out.println("Sum of Double: "+sum2);
    }
}
